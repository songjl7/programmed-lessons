public class AvgRainfall {
    public static void main(String[] args){
        double apr = 12;
        double may = 14;
        double jun = 8;
        double avg = (apr + may + jun) / 3;
        System.out.println("Rainfall for April:\t" + apr);
        System.out.println("Rainfall for May:\t" + may);
        System.out.println("Rainfall for June:\t" + jun);
        System.out.println("Average rainfall:\t" + avg);
    }
}